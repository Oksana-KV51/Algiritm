# Алгоритм действий для определения, является ли строка палиндромом:

#1. Прочитать строку, которую нужно проверить.
#2. Удалить из строки все пробелы и привести её к нижнему регистру для обеспечения корректного сравнения.
#3. Создать обратную версию строки и сравнить её с исходной очищенной строкой.
#4. Если исходная строка равна её обратной версии, то строка является палиндромом, иначе — нет.

def is_palindrome(s):
    # 1. Получить входную строку (в данном случае строка уже передана как аргумент)

    # 2. Очистить строку: удаляем пробелы и приводим к нижнему регистру
    cleaned_string = ''.join(s.split()).lower()

    # 3. Сравнить строку с её обратной версией
    reversed_string = cleaned_string[::-1]

    # 4. Вернуть результат
    return cleaned_string == reversed_string


# Пример использования
input_string = "А роза упала на лапу Азора"
print(is_palindrome(input_string))  # True